package com.alfanse.author.Services;

import com.alfanse.author.Utilities.SharedManagement;
import com.google.firebase.iid.FirebaseInstanceId;
import com.google.firebase.iid.FirebaseInstanceIdService;

/**
 * Created by Sachin on 12/30/2017.
 */

public class MyFirebaseInstanceIdService extends FirebaseInstanceIdService {

    @Override
    public void onTokenRefresh() {
        // Get updated InstanceID token.
        String refreshedToken = FirebaseInstanceId.getInstance().getToken();
        //Log.d(TAG, "Refreshed token: " + refreshedToken);
        SharedManagement.getInstance(getApplicationContext()).setString(SharedManagement.FIREBASE_TOKEN, refreshedToken);
    }
}
